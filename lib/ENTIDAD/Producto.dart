class Producto {
  num Codigo;
  String Numero;
  String Descripcion;
  String Talla;
  num Precio;
  String Imagen;

  Producto(num Codigo, String Numero, String Descripcion, String Talla,
      num Precio, String Imagen) {
    this.Codigo = Codigo;
    this.Numero = Numero;
    this.Descripcion = Descripcion;
    this.Talla = Talla;
    this.Precio = Precio;
    this.Imagen = Imagen;
  }
}
